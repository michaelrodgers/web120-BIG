  <h3 style="text-align:center;">Parallax</h3>
  <p>Parallax is a physical phenomenon that involves how perception changes over distance.  If you plotted two points on a grid with varying distances from a third point, calculated their angle to the vertical or horizontal axis of the third point, then shifted the third point and recalculated, you would find that the further the point is away, the less the angle changes and consequentially, the closer the points are, the more the angle changes.  Likewise, if two objects are placed at varying distances from an observer, and the observer then changes her position, the distance that the object appears to have moved within her field of vision is relative to their distance.  When used effectively with relative size (making objects in foreground larger than those further back), parallax principles can be applied to achieve a three-dimensional optical illusion on a two-dimensional screen..</p>
